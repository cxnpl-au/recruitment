/* 
  Randomly select a character from array.
  Add the randomly selected character to the password.
  Repeat this 20 times to create a 20 character password. 
*/
export const generateRandomPassword = () => {
  let text = "";

  for (var i = 0; i < 20; i++) {
    var randomCharacterIndex = Math.floor(Math.random() * characters.length);
    var randomCharacter = characters[randomCharacterIndex];
    text += randomCharacter;
  }

  return text;
};
